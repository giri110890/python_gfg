# '/' operator does floating point division for both int and float
# arguments

# A python 3 program to demonstrate use of 
# "/" for both integers ad floating points
print(5 / 2)
print(-5 / 2)
print( 5.0 / 2)
print(-5.0 / 2)

# Behaviour of "//" is same in both python 2.7 and python 3
print(5 // 2)
print(-5 // 2)
print(5.0 // 2)
print(-5.0 // 2)
